package com.fdmgroup.basicgame.physics;

public class VectorPath {
	private double x;
	private double y;
	public VectorPath(double xline, double yline) {
		x=xline;
		y=yline;
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	
	
}
